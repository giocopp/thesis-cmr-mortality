    print("✓ DATA CLEANING COMPLETE")
    print("="*70)
    print(f"\nCleaned files saved to: {output_dir}")
    print("\nFiles created:")
    for file in sorted(output_dir.glob("*.csv")):
        size_mb = file.stat().st_size / (1024 * 1024)
